
public class HelloWorld {

	public static void main(String[] args) {
		// 자바 파일을 만들때는 src 폴더에서 class메뉴를 누르면 된다.
		
		// 개발자만 보는 창 콘솔 console.log()
		// 자바에서는 System.out.println(); 로 변경된다.
		
		System.out.println("안녕하세요");
		System.out.println("실행은 단축키 사용이 편하다");
		System.out.println(1);
		
		// 단축키 syso + ctrl + space
		
		System.out.println(1 + 1);
		System.out.println(10.1 + 2);
		
		// 실행
		// 1. 메뉴 초록색 재생버튼
		// 2. 단축키 ctrl + f11 (저장 + 자바실행)

	}

}
