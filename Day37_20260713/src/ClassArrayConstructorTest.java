import java.util.ArrayList;

//쿠팡이츠,배민,요기요! 어플에서 주문내역!
//한명이 여러개의 주문 내역을 가지고 있다 
class 배달주문 {

	String 주문번호;
	String 주문한사람휴대폰번호;
	String 주문한음식명;

	public 배달주문(String 주문번호, String 주문한사람휴대폰번호, String 주문한음식명) {

		this.주문번호 = 주문번호;
		this.주문한사람휴대폰번호 = 주문한사람휴대폰번호;
		this.주문한음식명 = 주문한음식명;
	}

//결제를 하면 주문내역이 있어야된다. 결제내역, 주문내역에
//정보를 화면에 보여줄 수있다. 그러기때문에 데이터가 
//먼저 있어야된다. 
}

//도서관 - 대출 기록
//한 명이 여러 권을 대출할 수있다.
//이서희가 책을 빌리는데 자바책, 파이썬 빌린다.

class 대출기록 {
	String 대출번호;
	String 대출한사람이름;
	String 책제목;
	String 반납예정일;

	public 대출기록(String 대출번호, String 대출한사람이름, String 책제목, String 반납예정일) {

		this.대출번호 = 대출번호;
		this.대출한사람이름 = 대출한사람이름;
		this.책제목 = 책제목;
		this.반납예정일 = 반납예정일;
	}

}

public class ClassArrayConstructorTest {

	public static void main(String[] args) {
		
		// 이서희의 대출기록 목록을 저장한다.
		// 여러개의 기록을 한 곳에 저장하는 이유는! 관리!
		// 도서 기록들을 이용해서 도서를 관리하기 위해서 한 곳에서 저장한다.
		ArrayList<대출기록> 대출내역 = new ArrayList<>();

		대출기록 첫번째기록 = new 대출기록("L001", "김민수", "자바의 정석", "2026-07-27");

		대출내역.add(첫번째기록);
		대출내역.add(new 대출기록("L002", "이영희", "클린 코드", "2026-07-25"));
		대출내역.add(new 대출기록("L003", "김민수", "이펙티브 자바", "2026-08-01"));

		// 배열이 늘어났다 줄었다 자동으로 공간이 만들어지는
		// ArrayList 사용하면 된다.
		// 각각 저장하는 변수를 하나로 묶었다. 배달주문 타입으로 여러개의 변수를
		// 묶어서 한번에 저장한다.
		ArrayList<배달주문> 주문내역 = new ArrayList<>();

		// 주문내역[0] = new 배달주문("1234","010-1234-1234","마라탕")
		// 같은 의미다!

		주문내역.add(new 배달주문("1234", "010-1234-1234", "마라탕"));
		주문내역.add(new 배달주문("2580", "010-1234-1234", "떡볶이"));
		주문내역.add(new 배달주문("9512", "010-1234-1234", "짜장면"));

	}

}
