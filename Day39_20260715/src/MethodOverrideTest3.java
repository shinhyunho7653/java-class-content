class 알림 {

// 보내기 기능
	void 보내기() {
		System.out.println("알림을 보낸다.");
	}
}

class 이메일알림 extends 알림 {
	@Override
// 보내기 기능
	void 보내기() {
		System.out.println("새로운 메일 도착했습니다 확인하세요!");
	}
}

class 문자알림 extends 알림 {
	@Override
	void 보내기() {
		System.out.println("쇼핑몰에서 쿠폰이 도착했습니다.");
	}
}

