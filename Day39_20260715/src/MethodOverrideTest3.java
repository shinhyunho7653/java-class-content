// 회원가입 
//  - 이메일 가입 , 카카오 가입, 구글 가입 

class 회원가입 {
	String id;
	String pw;

	void 회원가입처리() {
		System.out.println("가입을 처리한다.");
	}
}

// 이메일로 회원가입 한다.
class 이메일가입 extends 회원가입 {

	void 회원가입처리() {
		System.out.println("이메일로 인증메일을 발송했습니다");
	}
}

// 카카오가입 회원가입이다!
class 카카오가입 extends 회원가입 {
	void 회원가입처리() {
		System.out.println("카카오 계정 정보를 가져와서 자동가입합니다.");
	}
}

// 구글가입 회원가입이다!
class 구글가입 extends 회원가입 {
	void 회원가입처리() {
		System.out.println("구글 계정 정보를 가져와서 자동가입합니다.");
	}
}
